uint8_t get_MOSFET_temperature(uint16_t MOSFETadc){

	uint8_t i;

	uint8_t index;

	for(i=0; i<111; i++){

		if(MOSFETadc <= MOSFET_temperature_ADC_array[i] && MOSFETadc > MOSFET_temperature_ADC_array[i+1]){
			index = i;
			break;
		}
	}
	if(MOSFETadc>2251)
	{
		index=0;
	}
	if(MOSFETadc<187)
	{
		index=110;
	}
	return MOSFET_temperature_array[index];
}



