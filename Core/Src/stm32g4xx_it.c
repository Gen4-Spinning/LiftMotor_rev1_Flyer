/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  if (S.motorState == ERROR_STATE){
  	  if (error_ms_counter >= 1000){
  		  HAL_GPIO_TogglePin(FAULT_LED_GPIO_Port, FAULT_LED_Pin);
  		  error_ms_counter = 0;
  		 S.errorMsgSentOnce = 0; // to make the msg get sent continuously
  	  }
    }
  GB.msTimer ++;
  if (GB.msTimer >= 1000){
	  if (GB.noisyRdngsPerSec > GB.maxNoiseRdngsSinceStart){
		  GB.maxNoiseRdngsSinceStart = GB.noisyRdngsPerSec;
	  }
	  GB.noisyRdngsPerSec = 0;
  }

  if (Dbg_multiStrk.dbg_multiStroke_On == 1){
	  Dbg_multiStrk.uart_ms_timer ++;
  }
  /* USER CODE END SysTick_IRQn 1 */
}

